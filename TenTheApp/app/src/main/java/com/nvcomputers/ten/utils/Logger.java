package com.nvcomputers.ten.utils;

import android.util.Log;

/**
 * Logs for whole app
 */

public class Logger {


    public static void showD(String message) {
        if (Const.DEBUG)
            Log.d(Const.APPNAME, message);
    }

    public static void showE(String message) {
        if (Const.DEBUG)
            Log.e(Const.APPNAME, message);
    }

    public static void showV(String message) {
        if (Const.DEBUG)
            Log.v(Const.APPNAME, message);
    }
}
